package Java01;

public class NumberFormatting2 {

    public static void main(String[] args){

        int a=20;
        int b=10;

        System.out.printf("Addition of two numbers is %d",(a+b));
        System.out.println();
        System.out.printf("Subtraction of two numbers is %d",(a-b));
        System.out.println();
        System.out.printf("multiplication of two numbers is %d",(a*b));
        System.out.println();
        System.out.printf("division of two numbers is %d",(a/b));
        System.out.println();
        System.out.printf("modulo division of two numbers is %d",(a%b));
        System.out.println();
    }
}
